    public PracticeTaskApplication(String title, double[] pressures, double[] times) {
        super(title);

        // Создание набора данных для графика
        XYSeriesCollection dataset = new XYSeriesCollection();
        XYSeries series = new XYSeries("Давление");

        // Заполнение серии данными давления
        for (int i = 0; i < pressures.length; i++) {
            series.add(times[i], pressures[i]);
        }

        dataset.addSeries(series);

        // Создание графика
        JFreeChart chart = ChartFactory.createXYLineChart(
                "График давления",  // Заголовок
                "Время",            // Ось X
                "Давление",        // Ось Y
                dataset             // Набор данных
        );

        // Настройка графика для отображения линий и точек
        XYPlot plot = chart.getXYPlot();
        XYItemRenderer renderer = plot.getRenderer();

        // Установка толщины линии
        renderer.setSeriesStroke(0, new BasicStroke(2.0f)); // Установите желаемую толщину линии
        // Установка формы точки
        renderer.setSeriesShape(0, new java.awt.geom.Ellipse2D.Double(-3, -3, 6, 6)); // Настройка формы точки

        // Добавление графика в панель
        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new Dimension(800, 600));
        setContentPane(chartPanel);
    }

